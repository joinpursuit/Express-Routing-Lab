// Math operation modules 

const add = (x, y) => {
   return  x + y 
}

const subtract = (x, y) => {
    return x - y
}

const multiply = (x, y) => {
    return x * y
}

const divide = (x, y) => {
    return x / y 
}

module.exports = {
    add: add,
    subtract: subtract,
    multiply: multiply,
    divide: divide,
}

